class Player{
    constructor(name,team,role,format,battingAverage,bowlingAverage,strikeRate,economy,matchesPlayed,
        totalInings,totalRuns,totalWickets,highestScore,bowlingSR,priceRate,totalPoints){
            this.name = name;
            this.team = team;
            this.format = format;
            this.role = role;
            this.battingAverage = battingAverage;
            this.bowlingAverage = bowlingAverage;
            this.strikeRate = strikeRate;
            this.economy = economy;
            this.matchesPlayed = matchesPlayed;
            this.totalInings = totalInings;
            this.totalRuns = totalRuns;
            this.totalWickets = totalWickets;
            this.highestScore = highestScore;
            this.bowlingSR = bowlingSR;
            this.price = priceRate,
            this.points = totalPoints;
    }
}